void DistribuePartieMaitre(TarotGame CurrentGame, int ChoixFixe)
{
int Num;
int MaxAge = 0;
int i;
char strMessage[64];
int Preneur = SUD;
int Decalage = 2;       //  Parties stockées avec joueur 0 = NORD

    if ( !ChoixFixe )
    {
        do {
            Num = rand() % NB_PARTIE_MAITRE;
        } while (MaitreJouee[Num] );
        if ( FlagPartieEnregistreeDefense && FlagPartieEnregistreeAttaque )
            Preneur = rand() % 4;               //  Preneur aléatoire
        else if (FlagPartieEnregistreeDefense)
            Preneur = rand() % 3 + 1;           //  Preneur forcément en défense
    }
    else
    {
        Num = CurrentGame->NumPartieEnregistree;
        Preneur = CurrentGame->PreneurPartieEnregistree;
    }
    Decalage = (Preneur + 2)&3;
    //  Incrémente "Age" des parties maîtres
    for ( i = 0; i < NB_PARTIE_MAITRE; i++ )
    {
        if ( MaitreJouee[i] ) MaitreJouee[i]++;
        if ( MaxAge < MaitreJouee[i] ) MaxAge = MaitreJouee[i];
    }
    NbPartieMaitreJouee++;
    MaitreJouee[Num] = 1;
    if ( MaxAge > NB_PARTIE_MAITRE*0.8 )
    {
        MaxAge -= 10;
        for ( i = 0; i < NB_PARTIE_MAITRE; i++ )
        {
            if ( MaxAge > MaitreJouee[i] ) MaitreJouee[i] = 0;      //  Remet dans le pool les parties anciennes
        }
    }
    //  Maintenant "distribue" le jeu
    memset(CurrentGame, 0, sizeof(*CurrentGame));       //  Efface la structure
    CurrentGame->FlagAfficheJoueur[0] = 1;              //  Par défaut SUD visible
    for ( i = 0; i < MAX_JOUEURS; i++ )
    {
        CurrentGame->CartePli[i] = -1;
        CurrentGame->AnnonceJoueur[i] = -1;
    }
    CurrentGame->NumPartieEnregistree = Num;
    //  La numérotation des joueurs a changé. Pour les donnes présentées ici dessus NORD = 0 alors qu'ici SUD = 0
    //  Effectue donc le changement, mais attention au chien qui reste en numéro 4
    CurrentGame->JoueurDistrib = (PartieMaitre[Num][0]+Decalage)&3;
    for ( i = 0; i < 78; i++ )
    {
        if ( PartieMaitre[Num][i+1] < 4 )
            CurrentGame->Carte2JoueurDistrib[i] = (PartieMaitre[Num][i+1]+Decalage)&3;
        else
            CurrentGame->Carte2JoueurDistrib[i] = 4;
    }
    for ( i = 0; i < 78; i++ )
    {
        CurrentGame->Carte2Joueur[i] = CurrentGame->Carte2JoueurDistrib[i];
    }
    RepartitionCarte(CurrentGame);
    EvalGames(CurrentGame);             //  Evalue la force des différents jeux
    CurrentGame->PreneurPartieEnregistree = Preneur;
    CurrentGame->StateJeu = JEU_ATTENTE_CONTRAT_J1;     //  Nouvel état jeu : attente décision joueur après preneur
    snprintf(strMessage, 60, "Partie n°%d", Num+1);
    AfficheMsgStatusBarGen(strMessage);
    for ( i = 0; i < 4; i++ )
        CurrentGame->FlagAfficheJoueur[i] = AfficheJeuJoueurs[i];
    ChangePartieMaitreConf();
    OutDebug("\n\n\n---------------------------------------------------------\n");
    OutDebug("Distribue partie maître %d, Preneur = %d\n", CurrentGame->NumPartieEnregistree+1, CurrentGame->PreneurPartieEnregistree);
    OutDebug("\n\n");
}

//  Retourne la "place" du Joueur dans cette partie
//  Le résultat va de 0 (dernier) à 1 premier. Si premier ex-aequo 0.9 et ainsi de suite

double ResultatPartieMaitre(TarotGame CurrentGame)
{
double res = 0.0;
int i;
int score = -CurrentGame->PointsDefense;

	for ( i = 0; i < 5; i++)
	{
		if ( ResultMaitre[CurrentGame->NumPartieEnregistree][i] < score ) res += 1.0;
		if ( ResultMaitre[CurrentGame->NumPartieEnregistree][i] == score ) res += 0.5;
	}
	return(res/5.0);
}
